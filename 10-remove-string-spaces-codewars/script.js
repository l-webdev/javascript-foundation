function noSpace(x) {
  return x.split(" ").join("");
}

const string1 = "8 j 8   mBliB8g  imjB8B8  jl  B";
console.log(noSpace(string1));
const string2 = "8 8 Bi fk8h B 8 BB8B B B  B888 c hl8 BhB fd";
console.log(noSpace(string2));
const string3 = "8aaaaa dddd r     ";
console.log(noSpace(string3));
